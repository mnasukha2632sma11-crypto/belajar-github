# 1. Tabung
r = float(input("Masukkan jari-jari tabung: "))
t_tabung = float(input("Masukkan tinggi tabung: "))
print(f"Volume Tabung: {3.14 * r**2 * t_tabung}\n")

# 2. Balok
p_balok = float(input("Masukkan panjang balok: "))
l_balok = float(input("Masukkan lebar balok: "))
t_balok = float(input("Masukkan tinggi balok: "))
print(f"Volume Balok: {p_balok * l_balok * t_balok}")